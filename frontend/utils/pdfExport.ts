/**
 * 4. Export Fuel & Expenses to PDF
 */
export function exportExpensePDF(
  records: any[],
  type: "FUEL" | "EXPENSE",
  filters?: Record<string, string>
) {
  if (!records || records.length === 0) {
    throw new Error("No data available to export.");
  }

  const headers = [
    "Vehicle",
    "Fuel Liters",
    "Fuel Cost",
    "Maintenance Cost",
    "Toll Charges",
    "Other Expenses",
    "Operational Cost",
    "Date"
  ];

  const rows = records.map((r) => {
    if (type === "FUEL") {
      const fuelLiters = Number(r.liters || 0);
      const fuelCost = Number(r.fuelCost || 0);
      return [
        r.vehicle?.regNo || "—",
        `${fuelLiters.toLocaleString()} L`,
        formatCurrency(fuelCost),
        "$0",
        "$0",
        "$0",
        formatCurrency(fuelCost),
        formatDate(r.logDate)
      ];
    } else {
      const tollCost = Number(r.tollCost || 0);
      const otherCost = Number(r.otherCost || 0);
      const opCost = tollCost + otherCost;
      return [
        r.vehicle?.regNo || "—",
        "0 L",
        "$0",
        "$0", // Maintenance Cost is 0 unless linked
        formatCurrency(tollCost),
        formatCurrency(otherCost),
        formatCurrency(opCost),
        formatDate(r.createdAt)
      ];
    }
  });

  const today = new Date().toISOString().split("T")[0];
  generatePDFReport(
    type === "FUEL" ? "Fuel Consumption Report" : "Trip Expenditures Report",
    headers,
    rows,
    `expenses-report-${today}.pdf`,
    filters
  );
}

/**
 * 5. Export Analytics to PDF
 */
export function exportAnalyticsPDF(vehicles: any[], filters?: Record<string, string>) {
  if (!vehicles || vehicles.length === 0) {
    throw new Error("No data available to export.");
  }

  const headers = [
    "Vehicle",
    "Fleet Utilization %",
    "Fuel Efficiency (km/L)",
    "Fuel Cost",
    "Maintenance Cost",
    "Operational Cost",
    "Revenue",
    "Vehicle ROI %"
  ];

  const rows = vehicles.map((v) => {
    // 1. Fleet Utilization
    const fleetUtilizationStr = v.status === "ON_TRIP" ? "100%" : "0%";

    // 2. Fuel Efficiency
    // total actual distance / total liters
    const totalDistance = v.trips
      ? v.trips.filter((t: any) => t.status === "COMPLETED").reduce((sum: number, t: any) => sum + Number(t.actualDistanceKm || 0), 0)
      : 0;
    const totalLiters = v.fuelLogs
      ? v.fuelLogs.reduce((sum: number, l: any) => sum + Number(l.liters || 0), 0)
      : 0;
    const fuelEfficiencyStr = totalLiters > 0 ? `${(totalDistance / totalLiters).toFixed(2)} km/L` : "0.00 km/L";

    // 3. Costs
    const fuelCost = Number(v.fuelCost || 0);
    const maintenanceCost = Number(v.maintenanceCost || 0);
    const operationalCost = Number(v.operationalCost || 0);

    // 4. Revenue
    const totalRevenue = v.trips
      ? v.trips.reduce((sum: number, t: any) => sum + Number(t.revenue?.revenue || 0), 0)
      : 0;

    // 5. ROI %
    let roiStr = "0.0%";
    const acqCost = Number(v.acquisitionCost || 0);
    if (acqCost > 0) {
      const roi = ((totalRevenue - (fuelCost + maintenanceCost)) / acqCost) * 100;
      roiStr = `${roi.toFixed(1)}%`;
    }

    return [
      v.regNo || "—",
      fleetUtilizationStr,
      fuelEfficiencyStr,
      formatCurrency(fuelCost),
      formatCurrency(maintenanceCost),
      formatCurrency(operationalCost),
      formatCurrency(totalRevenue),
      roiStr
    ];
  });

  const today = new Date().toISOString().split("T")[0];
  generatePDFReport("Fleet Analytics & ROI Report", headers, rows, `analytics-report-${today}.pdf`, filters);
}
